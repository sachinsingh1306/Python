# Comparision Operation performed

a = int(input("Enter a Number...."))
b = int(input("Enter b Number...."))

if a == b:
    print("a is equals to b")
elif a != b:
    print("a is not equals to b")
elif a > b:
    print("a is greater than b")
elif a < b:
    print("a is smaller than b")
else:
    print("Invalid Input ")